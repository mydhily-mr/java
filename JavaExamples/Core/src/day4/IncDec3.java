package day4;

public class IncDec3
{
    public static void main(final String[] args) {
        int a = 13;
        System.out.println(++a);
        System.out.println(++a);
        System.out.println(a++);
        System.out.println(--a);
        System.out.println(++a);
        System.out.println(--a);
        System.out.println(a--);
        System.out.println(a);
    }
}