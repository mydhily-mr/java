package day12;

public class Charger
{
    String brand;
    String color;
    
    public Charger(final String brand, final String color) {
        this.brand = brand;
        this.color = color;
    }
    
    public String getbrand() {
        return this.brand;
    }
    
    public String getcolor() {
        return this.color;
    }
}